import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), "..");
const jsonPath = path.join(root, "data/default-categories-current.json");

const data = JSON.parse(fs.readFileSync(jsonPath, "utf8"));

function cat(id) {
  const c = data.find((x) => x.id === id);
  if (!c) throw new Error(`missing category ${id}`);
  return c;
}

function addKw(id, ...words) {
  const c = cat(id);
  const set = new Set(c.keywords.map((w) => w.toLowerCase()));
  for (const w of words) {
    const t = w.trim().toLowerCase();
    if (t) set.add(t);
  }
  c.keywords = [...set];
}

function removeKw(id, ...words) {
  const c = cat(id);
  const drop = new Set(words.map((w) => w.toLowerCase()));
  c.keywords = c.keywords.filter((w) => !drop.has(w.toLowerCase()));
}

function replaceKw(id, remove, add) {
  removeKw(id, ...remove);
  addKw(id, ...add);
}

