export type DictKey = keyof (typeof DICT)["ru"];

/** Русское склонение: 1 запись, 2 записи, 5 записей */
export function ruPlural(
  count: number,
  one: string,
  few: string,
  many: string,
): string {
  const abs = Math.abs(count) % 100;
  const mod10 = abs % 10;
  if (abs > 10 && abs < 20) return many;
  if (mod10 > 1 && mod10 < 5) return few;
  if (mod10 === 1) return one;
  return many;
}

export function enPlural(count: number, one: string, many: string): string {
  return Math.abs(count) === 1 ? one : many;
}

export function formatDaysLabel(count: number, locale: Locale): string {
  return locale === "ru"
    ? `${count} ${ruPlural(count, "день", "дня", "дней")}`
    : `${count} ${enPlural(count, "day", "days")}`;
}

export function t(
  locale: Locale,
  key: DictKey,
  vars?: Record<string, string>,
): string {
  let text: string = DICT[locale][key] ?? DICT.ru[key] ?? String(key);
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v);
    });
  }
  return text;
}

export function detectLocale(languageCode?: string): Locale {
  if (!languageCode) {
    if (
      typeof navigator !== "undefined" &&
      navigator.language.startsWith("ru")
    ) {
      return "ru";
    }
    return "en";
  }
  return languageCode.startsWith("ru") ? "ru" : "en";
}
